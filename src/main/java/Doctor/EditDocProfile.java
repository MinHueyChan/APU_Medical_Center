    private void loadDoctorData(){
        if (currentUser != null) {
            txtUserID.setText(currentUser.getUserId());
            txtUserID.setEditable(false);
            txtUsername.setText(currentUser.getUsername());
            txtUsername.setEditable(false);
            txtName.setText(currentUser.getName());
            cmbGender.setSelectedItem(currentUser.getGender());
            txtEmail.setText(currentUser.getEmail());
            txtPhone.setText(currentUser.getPhone());
            txtAge.setText(String.valueOf(currentUser.getAge()));

            // View-only on this page; editing happens in dialog
            txtUserID.setEditable(false);
            txtUsername.setEditable(false);
            txtName.setEditable(false);
            txtEmail.setEditable(false);
            txtPhone.setEditable(false);
            txtAge.setEditable(false);
            cmbGender.setEnabled(false);
        }
    }
    
    private void refreshDoctorData() {
        loadDoctorData();
    }

    
