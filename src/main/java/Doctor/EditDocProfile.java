    private void ChangePasswordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangePasswordBtnActionPerformed
        try {
            String currentPasswordText = currentPassword.getText().trim();
            String newPasswordText = newPassword.getText().trim();
            String confirmPasswordText = confirmPassword.getText().trim();

            // Validate input
            if (currentPasswordText.isEmpty() || newPasswordText.isEmpty() || confirmPasswordText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in all password fields!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check if current password is correct
            if (!currentPasswordText.equals(currentUser.getPassword())) {
                JOptionPane.showMessageDialog(this, "Current password is incorrect!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check if new password and confirm password match
            if (!newPasswordText.equals(confirmPasswordText)) {
                JOptionPane.showMessageDialog(this, "New password and confirm password do not match!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check password length
            if (newPasswordText.length() < 4) {
                JOptionPane.showMessageDialog(this, "New password must be at least 4 characters long!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Update password
            currentUser.setPassword(newPasswordText);

            // Update doctor data in DataIO
            for (int i = 0; i < DataIO.allDoctors.size(); i++) {
                if (DataIO.allDoctors.get(i).getUserId().equals(currentUser.getUserId())) {
                    DataIO.allDoctors.set(i, currentUser);
                    break;
                }
            }

            // Save to File
            DataIO.write();

            // Clear password fields
            currentPassword.setText("");
            newPassword.setText("");
            confirmPassword.setText("");

            JOptionPane.showMessageDialog(this, "Password changed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error changing password: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ChangePasswordBtnActionPerformed

