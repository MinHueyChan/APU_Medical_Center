    private void loadAppointmentsToComboBox() {
        try {
            CmbAppointmentID.removeAllItems();
            for (main.Appointment a : DataIO.allAppointments) {
                if (a.getCustomerUsername().equals(customer.getUsername())
                        && (a.getComment() == null || a.getComment().isEmpty())) {
                    CmbAppointmentID.addItem(a.getAppointmentId());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading appointments.");
            e.printStackTrace();
        }
    }

    private void setupComboBoxListener() {
        CmbAppointmentID.addActionListener(e -> {
            String selectedId = (String) CmbAppointmentID.getSelectedItem();
            if (selectedId != null) {
                for (main.Appointment a : DataIO.allAppointments) {
                    if (a.getAppointmentId().equals(selectedId)) {
                        txtDate.setText(a.getDate());
                        txtTime.setText(a.getTime());
                        txtDoctor.setText(a.getDoctorUsername());
                        txtStaff.setText(a.getStaffId());
                        break;
                    }
                }
            }
        });
    }

