    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {
        String selectedAppointment = (String) CmbAppointmentID.getSelectedItem();
        if (selectedAppointment == null) {
            JOptionPane.showMessageDialog(this, "Please select an appointment.");
            return;
        }

        String comment = CommentBox.getText().trim();
        if (comment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a comment.");
            return;
        }

        Integer rating = (Integer) SpnRatings.getValue();
        if (rating == null || rating < 1 || rating > 5) {
            JOptionPane.showMessageDialog(this, "Please select a valid rating (1-5).");
            return;
        }


        DataIO.updateCustomerComment(selectedAppointment, rating, comment);

        JOptionPane.showMessageDialog(this, "Comment saved successfully!");

        if (onCommentSaved != null) {
            onCommentSaved.accept(selectedAppointment);
        }

        this.dispose();
    }

