    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        String keyword = txtSearch.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) Historytable.getModel();
        model.setRowCount(0);

        for (main.Appointment a : DataIO.allAppointments) {
            if (a.getCustomerUsername().equals(customer.getUsername())
                    && ("completed".equalsIgnoreCase(a.getStatus())
                    || "cancelled".equalsIgnoreCase(a.getStatus()))) {
                if (a.getAppointmentId().toLowerCase().contains(keyword)
                        || a.getStatus().toLowerCase().contains(keyword)
                        || a.getType().toLowerCase().contains(keyword)) {

                    model.addRow(new Object[]{
                        a.getAppointmentId(),
                        a.getStatus(),
                        a.getType(),
                        a.getCharges(),
                        a.getComment() != null ? a.getComment() : "",
                        a.getFeedback() != null ? a.getFeedback() : ""
                    });
                }
            }
        }
    }//GEN-LAST:event_SearchActionPerformed

