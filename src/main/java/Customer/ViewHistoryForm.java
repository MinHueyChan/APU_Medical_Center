    private void loadHistoryTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) Historytable.getModel();
            model.setRowCount(0);

            for (main.Appointment a : DataIO.allAppointments) {
                if (a.getCustomerUsername().equals(customer.getUsername())
                        && ("completed".equalsIgnoreCase(a.getStatus())
                        || "cancelled".equalsIgnoreCase(a.getStatus()))) {
                    Object[] row = {
                        a.getAppointmentId(),
                        a.getStatus(),
                        a.getType(),
                        a.getCharges(),
                        a.getComment() != null ? a.getComment() : "",
                        a.getFeedback() != null ? a.getFeedback() : ""

                    };
                    model.addRow(row);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading history.");
            e.printStackTrace();

        }
    }

    private void addTableClickListener() {
        Historytable.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = Historytable.rowAtPoint(evt.getPoint());
                int col = Historytable.columnAtPoint(evt.getPoint());
                if (row < 0 || col < 0) {
                    return;
                }

                String colName = Historytable.getColumnModel()
                        .getColumn(col)
                        .getHeaderValue()
                        .toString();

                if ("Comment".equalsIgnoreCase(colName) || "Feedback".equalsIgnoreCase(colName)) {
                    Object value = Historytable.getValueAt(row, col);
                    String text = value == null ? "" : value.toString();

                    JTextArea textArea = new JTextArea(text);
                    textArea.setLineWrap(true);
                    textArea.setWrapStyleWord(true);
                    textArea.setEditable(false);

                    JScrollPane scrollPane = new JScrollPane(textArea);
                    scrollPane.setPreferredSize(new Dimension(400, 200));

                    JOptionPane.showMessageDialog(
                            ViewHistoryForm.this,
                            scrollPane,
                            colName + " Detail",
                            JOptionPane.INFORMATION_MESSAGE
                    );
                }
            }
        });
    }

