    private void loadAppointments() {
        try {
            DefaultTableModel model = (DefaultTableModel) Appointmenttable.getModel();
            model.setRowCount(0);

            for (main.Appointment a : DataIO.allAppointments) {
                if (a.getCustomerUsername().equals(customer.getUsername())
                        && ("Booked".equalsIgnoreCase(a.getStatus())
                        || ("Pending".equalsIgnoreCase(a.getStatus()))
                        || "Cancellation In Progress".equalsIgnoreCase(a.getStatus()))) {

                    Object[] row = {
                        a.getAppointmentId(),
                        a.getDate(),
                        a.getTime(),
                        a.getDoctorUsername(),
                        a.getStaffId(),
                        a.getStatus()
                    };
                    model.addRow(row);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading appointments.");
            e.printStackTrace();
        }
    }

