    private void CancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelActionPerformed
        int selectedRow = Appointmenttable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an appointment first.");
            return;
        }

        String apptId = Appointmenttable.getValueAt(selectedRow, 0).toString();
        String status = Appointmenttable.getValueAt(selectedRow, 5).toString();

        if (!status.equalsIgnoreCase("Pending")) {
            JOptionPane.showMessageDialog(this, "Only Pending appointments can be cancelled.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Appointment ID: " + apptId
                + "\nConfirm request cancellation?",
                "Request Cancellation", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                DataIO.updateAppointmentStatus(apptId, "Cancellation In Progress");
                JOptionPane.showMessageDialog(this, "Request submitted.");
                loadAppointments();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error updating appointment.");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_CancelActionPerformed


    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        String keyword = txtSearch.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) Appointmenttable.getModel();
        model.setRowCount(0);

        boolean found = false;

        for (main.Appointment a : DataIO.allAppointments) {
            if (a.getCustomerUsername().equals(customer.getUsername())
                    && ("Booked".equalsIgnoreCase(a.getStatus())
                    || "Pending".equalsIgnoreCase(a.getStatus())
                    || "Cancellation In Progress".equalsIgnoreCase(a.getStatus()))) {

                String apptId = (a.getAppointmentId() == null) ? "" : a.getAppointmentId().toLowerCase();
                String doctor = (a.getDoctorUsername() == null) ? "" : a.getDoctorUsername().toLowerCase();
                String status = (a.getStatus() == null) ? "" : a.getStatus().toLowerCase();

                if (keyword.isEmpty()
                        || apptId.contains(keyword)
                        || doctor.contains(keyword)
                        || status.contains(keyword)) {

                    model.addRow(new Object[]{
                        a.getAppointmentId(),
                        a.getDate(),
                        a.getTime(),
                        a.getDoctorUsername() == null ? "" : a.getDoctorUsername(),
                        a.getStaffId() == null ? "" : a.getStaffId(),
                        a.getStatus() == null ? "Unknown" : a.getStatus()
                    });
                    found = true;
                }
            }
        }
    }//GEN-LAST:event_SearchActionPerformed

