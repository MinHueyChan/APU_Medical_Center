    private void loadCreatedComments() {
        DefaultTableModel model = (DefaultTableModel) Commenttable.getModel();
        model.setRowCount(0);

        if (customer != null) {
            for (main.Appointment a : DataIO.allAppointments) {
                if (a.getCustomerUsername().equals(customer.getUsername())
                        && a.getComment() != null && !a.getComment().isEmpty()
                        && a.getRating() != null) {

                    Object[] row = {
                        a.getAppointmentId(),
                        a.getDoctorUsername(),
                        a.getStaffId(),
                        a.getDate(),
                        a.getTime(),
                        a.getRating(),
                        a.getComment()
                    };
                    model.addRow(row);
                }
            }
        }
    }
        private void addTableClickListener() {
        Commenttable.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = Commenttable.rowAtPoint(evt.getPoint());
                int col = Commenttable.columnAtPoint(evt.getPoint());
                if (row < 0 || col < 0) return;

                String colName = Commenttable.getColumnModel()
                                             .getColumn(col)
                                             .getHeaderValue()
                                             .toString();

                if ("Comment".equalsIgnoreCase(colName)) {
                    Object value = Commenttable.getValueAt(row, col);
                    String text = value == null ? "" : value.toString();

                    JTextArea textArea = new JTextArea(text);
                    textArea.setLineWrap(true);
                    textArea.setWrapStyleWord(true);
                    textArea.setEditable(false);
                    textArea.setCaretPosition(0);

                    JScrollPane scrollPane = new JScrollPane(textArea);
                    scrollPane.setPreferredSize(new Dimension(400, 200));

                    JOptionPane.showMessageDialog(
                        CusCommentForm.this,
                        scrollPane,
                        "Comment Detail",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                }
            }
        });
    }

