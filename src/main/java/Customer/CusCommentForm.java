    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        int selectedRow = Commenttable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a comment to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this comment?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            String appointmentId = (String) Commenttable.getValueAt(selectedRow, 0);

            for (main.Appointment appt : DataIO.allAppointments) {
                if (appt.getAppointmentId().equals(appointmentId)) {
                    appt.setFeedback(null);
                    appt.setRating(null);
                    appt.setComment(null);
                    break;
                }
            }

            DataIO.write();

            loadCreatedComments();

            JOptionPane.showMessageDialog(this, "Comment deleted successfully!");
        }
    }//GEN-LAST:event_DeleteActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        int selectedRow = Commenttable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a comment to update.");
            return;
        }

        String appointmentId = (String) Commenttable.getValueAt(selectedRow, 0);

        for (main.Appointment appt : DataIO.allAppointments) {
            if (appt.getAppointmentId().equals(appointmentId)) {
                CusUpdateComment dialog = new CusUpdateComment(customer, appt, (updatedAppt) -> {

                    loadCreatedComments();
                });
                dialog.setVisible(true);
                break;
            }
        }
    }//GEN-LAST:event_UpdateActionPerformed

    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        String keyword = txtSearch.getText().trim().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) Commenttable.getModel();
        model.setRowCount(0);

        for (main.Appointment a : DataIO.allAppointments) {
            if (a.getCustomerUsername().equals(customer.getUsername())
                    && a.getComment() != null && !a.getComment().isEmpty()
                    && a.getRating() != null) {
        String doctor = (a.getDoctorUsername() == null) ? "" : a.getDoctorUsername().toLowerCase();
        String ratingStr = String.valueOf(a.getRating()).toLowerCase();

        if (keyword.isEmpty()
                || a.getAppointmentId().toLowerCase().contains(keyword)
                || doctor.contains(keyword)
                || ratingStr.contains(keyword)) {
            
                    model.addRow(new Object[]{
                        a.getAppointmentId(),
                        (a.getDoctorUsername()== null) ? "" : a.getDoctorUsername(),
                        a.getStaffId(),
                        a.getDate(),
                        a.getTime(),
                        a.getRating(),
                        a.getComment(),});
                }
            }
        }
    }//GEN-LAST:event_SearchActionPerformed

